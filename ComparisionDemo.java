public class ComparisionDemo {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;
        if(value1 == value2){
            System.out.println("value1 is equal to value2");
        }
        if(value1 != value2){
            System.out.println("value1 is not equal to value2");
        }
        if(value1 > value2){
            System.out.println("value1 > value2");
        }
        if(value1 < value2) {
            System.out.println("value1 < value2");
        }
        if(value1 <= value2) {
            System.out.println("value1 <= value2");
        }
    }
}
